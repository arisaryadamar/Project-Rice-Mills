package com.project.ricemills.beras.model;

public class Beras_Model {
    String kode, kode_barang, ukuran_sak, stok;

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getKode_barang() {
        return kode_barang;
    }

    public void setKode_barang(String kode_barang) {
        this.kode_barang = kode_barang;
    }

    public String getUkuran_sak() {
        return ukuran_sak;
    }

    public void setUkuran_sak(String ukuran) {
        this.ukuran_sak = ukuran;
    }

    public String getStok() {
        return stok;
    }

    public void setStok(String stok) {
        this.stok = stok;
    }
}
